namespace KD.AutoBot.Input.Windows.Native
{
    public enum WindowsMouseData : uint
    {
        /// <summary>
        /// Set if the first X button was pressed or released.
        /// </summary>
        XBUTTON1 = 0x0001,

        /// <summary>
        /// Set if the second X button was pressed or released.
        /// </summary>
        XBUTTON2 = 0x0002
    }
}